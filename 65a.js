
function numberOperator(a, b) {
    switch (a, b) {
        case 1:
            return a + b;
            break;
        case 2:
            return a - b;
            break;
        case 3:
            return a * b;
            break;
        case 4:
            return a / b;
            break;
          

    }

}
console.log(numberOperator(2))